/**
 * Client Side Script
 * 
 * Client-side JavaScript is made up of two parts, a fundamental language and predefined objects for performing JavaScript 
 * in a browser. JavaScript for the client is automatically included in the HTML pages. At runtime, the browser understands this script.
 */

/**
 * Server Side Script
 * 
 * Server-side JavaScript, involves the execution of JavaScript code on a server in response to client requests. 
 * It handles these requests and delivers the relevant response to the client, which may include client-side JavaScript for subsequent execution within the browser.
 */